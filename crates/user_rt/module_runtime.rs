pub struct KazuWriter;

impl Write for KazuWriter {
    fn write_str(&mut self, s: &str) -> core::fmt::Result {
        sys_write_raw(s.as_bytes());
        Ok(())
    }
}

#[macro_export]
macro_rules! print {
    ($($arg:tt)*) => {{
        use core::fmt::Write;
        let _ = write!(KazuWriter, $($arg)*);
    }};
}

#[macro_export]
macro_rules! println {
    () => { sys_write_raw(b"\r\n") };
    ($($arg:tt)*) => {{
        use core::fmt::Write;
        let _ = write!(KazuWriter, $($arg)*);
        sys_write_raw(b"\r\n");
    }};
}

/// Metadata returned by kkm_info().
#[repr(C)]
pub struct KkmInfo {
    pub name:    &'static str,
    /// Null-terminated list of module names this module depends on.
    pub depends: &'static [&'static str],
}

/// Module entry point generated by module_runtime. Calls the module's four
/// lifecycle functions in order:
///   kkm_info()  — metadata query (not called here, used by build tooling)
///   kkm_init()  — hardware initialisation, returns false on failure
///   kkm_run()   — main event loop, returns when a stop signal is received
///   kkm_exit()  — cleanup
#[no_mangle]
pub extern "C" fn _start(_argc: u64, _argv: u64) -> ! {
    // Enable signal catching so kmod::unload() can reach us.
    sys_signal_catch(true);

    if !kkm_init() {
        sys_exit(1);
    }

    kkm_run();
    kkm_exit();
    sys_exit(0);
}

#[panic_handler]
fn panic_handler(info: &core::panic::PanicInfo) -> ! {
    print!("[KKM PANIC] ");
    let _ = write!(KazuWriter, "{}", info.message());
    print!("\r\n");
    sys_exit(1)
}

#[global_allocator]
static ALLOC: KazuAlloc = KazuAlloc;

struct KazuAlloc;

unsafe impl core::alloc::GlobalAlloc for KazuAlloc {
    unsafe fn alloc(&self, layout: core::alloc::Layout) -> *mut u8 {
        let ptr = sys_heap_alloc(layout.size() as u64);
        if ptr == 0 { return core::ptr::null_mut(); }
        ptr as *mut u8
    }

    unsafe fn dealloc(&self, ptr: *mut u8, _layout: core::alloc::Layout) {
        sys_heap_free(ptr as u64);
    }
}

