/// Returns true if a stop signal is pending (clears the flag).
pub fn sys_signal_check() -> bool {
    let r: u64;
    unsafe {
        core::arch::asm!(
            "int 0x80",
            inlateout("rax") SYS_SIGNAL_CHECK => r,
            in("rdi") 0u64,
            in("rsi") 0u64,
            in("rdx") 0u64,
        );
    }
    r != 0
}

