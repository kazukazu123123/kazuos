pub const GLYPH_SIZE: usize = 14;
pub const GLYPH_WIDTH: usize = 7;
pub const GLYPH_HEIGHT: usize = 14;
pub const SCALE: usize = 2;
pub const CHAR_WIDTH: usize = GLYPH_WIDTH * SCALE;
pub const CHAR_HEIGHT: usize = GLYPH_HEIGHT * SCALE;
pub const ASCENT: usize = 11 * SCALE;

pub fn draw_glyph(fb: &FramebufferInfo, c: char, x: usize, baseline_y: usize, r: u8, g: u8, b: u8) {
    let code = c as u8;
    if code < 0x20 || code > 0x7E {
        return;
    }
    let idx = (code - 0x20) as usize * GLYPH_SIZE;
    let glyph = &FONT_7x14[idx..idx + GLYPH_SIZE];

    let top = baseline_y.saturating_sub(ASCENT);

    for row in 0..GLYPH_HEIGHT {
        let bits = glyph[row];
        for col in 0..GLYPH_WIDTH {
            if bits & (0x80 >> col) != 0 {
                let px = x + col * SCALE;
                let py = top + row * SCALE;
                for dy in 0..SCALE {
                    for dx in 0..SCALE {
                        unsafe {
                            fb.write_pixel(px + dx, py + dy, r, g, b);
                        }
                    }
                }
            }
        }
    }
}
